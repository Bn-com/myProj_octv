import maya.cmds as cmds
from McdGeneral import *
from McdSimpleCmd import *

import McdAgentParticleEmitter
reload(McdAgentParticleEmitter)
from McdAgentParticleEmitter import *


def McdAgentParticleEmitterGUI():
    
    winName = "McdAgentParticleEmitter"
    if cmds.window(winName, ex = True):
        cmds.deleteUI(winName)
    cmds.window(winName, title = "Agent Particle Emitter Manager",rtf =True,menuBar=True, width=200)
    
    cmds.menu( label='Options')
    cmds.menuItem( label='Refresh contents', c = "McdRefreshAgentParticleEmitter()")
    cmds.menuItem( label='Help' )
    cmds.menuItem( divider=True )
    cmds.menuItem( label='Exit', c = "McdExitAgentParticleEmitter()" )
    
    form = cmds.formLayout()
    tabs = cmds.tabLayout(innerMarginWidth=5, innerMarginHeight=5)
    cmds.formLayout( form, edit=True, attachForm=((tabs, 'top', 0), (tabs, 'left', 0), (tabs, 'bottom', 0), (tabs, 'right', 0)) )
    
    #-------------------------  Initialize  -------------------------#
    ## get contents here:
    info = McdEmitterStartUpSetup()
    
    #--------------------------  Main GUI  --------------------------#
    child0 = cmds.columnLayout(adj = True)
    cmds.rowColumnLayout(nc = 6, cw = [(1,120),(2,10),(3,120),(4, 10),(5, 170)])
    cmds.button(l = "Mark Emitter", c = "McdMarkEmitter()")
    cmds.text(l ="")
    cmds.button(l = "Unmark Emitter", c = "McdUnMarkEmitter()")
    
    cmds.setParent("..")
    
    #(4,80)
    cmds.rowColumnLayout(nc = 14, cw = [(1,60),(2,60),(3,80),(4,80),(5, 40), (6, 40),(7, 40),(8, 60),(9, 60),(10, 60),(11, 60),(12, 60),(13, 60),(14, 60)])
    cmds.text(l = "Emitter", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Particle", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Action Name", align = "left", font = "smallBoldLabelFont")
    #cmds.text(l = "Action Group", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Bone Name", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Start", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "End", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Mode", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Dir X", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Dir Y", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Dir Z", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Dir Rand", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Rate", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Speed", align = "left", font = "smallBoldLabelFont")
    
    cmds.text(l ="")
    
    # fill contents here:
    if info != []:
        for i in range(len(info[0])):
            stri = str(i)
            cmds.textField("emitter_tf" + stri, tx = info[1][i], ed = False)
            cmds.textField("particleShape_tf" + stri, tx = info[0][i], ed = False)
            cmds.textField("actionName_tf" + stri, tx = info[2][i], cc = "changeEmitterAN("+stri+")")
            #cmds.textField("actionGroup_tf" + stri, tx = info[3][i], cc = "changeEmitterAG("+stri+")")
            cmds.textField("boneName_tf" + stri, tx = info[4][i], cc = "changeEmitterBN("+stri+")")
            cmds.intField("startFrame_ff" + stri, v = info[5][i], cc = "changeEmitterSF("+stri+")")
            cmds.intField("endFrame_ff" + stri, v = info[6][i], cc = "changeEmitterEF("+stri+")")
            cmds.intField("emitMode_ff" + stri, v = info[7][i], cc = "changeEmitterEM("+stri+")")
            cmds.floatField("emitX_ff" + stri, v = info[8][i], cc = "changeEmitterEX("+stri+")")
            cmds.floatField("emitY_ff" + stri, v = info[9][i], cc = "changeEmitterEY("+stri+")")
            cmds.floatField("emitZ_ff" + stri, v = info[10][i], cc = "changeEmitterEZ("+stri+")")
            cmds.floatField("emitR_ff" + stri, v = info[11][i], cc = "changeEmitterER("+stri+")")
            cmds.floatField("rate_ff" + stri, v = info[12][i], cc = "changeEmitterRT("+stri+")")
            cmds.floatField("speed_ff" + stri, v = info[13][i], cc = "changeEmitterSP("+stri+")")
            cmds.button(l = "Select", c = "McdSelectEmitter("+ stri +")")
    
    cmds.setParent("..")
    
    
    cmds.setParent("..")
    
    
    #------------------------------- Cricial Help --------------------------------#
    child1 = cmds.columnLayout(adj = True)
    cmds.text(l = "* List all the agent emitter in scene", fn = "smallBoldLabelFont", align = "left")
    
    cmds.separator(h = 10)
    cmds.button(l = "Check detailed help")
    
    cmds.setParent( '..' )
    
    
    cmds.tabLayout( tabs, edit=True,tabLabel=((child0, "Agent Emitter in Scene"),(child1, "Quick Tips")))
    cmds.showWindow(winName)



def McdRefreshAgentParticleEmitter():
    McdAgentParticleEmitterGUI()

def McdExitAgentParticleEmitter():
    try:
        cmds.deleteUI("McdAgentParticleEmitter")
    except:
        pass


