import maya.cmds as cmds
from McdGeneral import *
from McdSimpleCmd import *

import McdGeoVisManager
reload(McdGeoVisManager)
from McdGeoVisManager import *


def McdGeoVisManagerGUI():
    
    winName = "McdGeoVisManager"
    if cmds.window(winName, ex = True):
        cmds.deleteUI(winName)
    cmds.window(winName, title = "Geometry Hide Manager",rtf =True,menuBar=True, width=200)
    
    cmds.menu( label='Options')
    cmds.menuItem( label='Refresh contents', c = "McdRefreshGeoVisManager()")
    cmds.menuItem( label='Help' )
    cmds.menuItem( divider=True )
    cmds.menuItem( label='Exit', c = "McdExitGeoVisManager()" )
    
    form = cmds.formLayout()
    tabs = cmds.tabLayout(innerMarginWidth=5, innerMarginHeight=5)
    cmds.formLayout( form, edit=True, attachForm=((tabs, 'top', 0), (tabs, 'left', 0), \
                                                (tabs, 'bottom', 0), (tabs, 'right', 0)) )
    
    #-------------------------  Initialize  -------------------------#
    ## get contents here:
    info = McdGeoVisManagerStartUpSetup()
    
    #--------------------------  Main GUI  --------------------------#
    child0 = cmds.columnLayout(adj = True)

    
    cmds.rowColumnLayout(nc = 6, cw = [(1,120),(2,120),(3,100),(4,90),(5,80),(6, 55)])
    cmds.text(l = "GVI Node", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Geometry Name", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Action Name", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "Start Frame", align = "left", font = "smallBoldLabelFont")
    cmds.text(l = "End Frame", align = "left", font = "smallBoldLabelFont")
    cmds.text(l ="")
    
    # fill contents here:
    if info != []:
        for i in range(len(info[0])):
            stri = str(i)
            cmds.textField("GVINode_tf" + stri, tx = info[0][i], ed = False)
            cmds.textField("geoName_tf" + stri, tx = info[1][i], cc = "changeGVIGN(" +stri+ ")")
            cmds.textField("actionName_tf" + stri, tx = info[2][i], cc = "changeGVIAN(" +stri+ ")")
            cmds.intField("startFrame_ff" + stri, v = info[3][i], cc = "changeGVISF("+stri+")")
            cmds.intField("endFrame_ff" + stri, v = info[4][i], cc = "changeGVIEF("+stri+")")
            cmds.button(l = "Delete", c = "deleteGVINode("+ stri +")")
    
    cmds.setParent("..")
    
    cmds.rowColumnLayout(nc = 6, cw = [(1,120),(2,10),(3,120),(4, 10),(5, 170)])
    cmds.button(l = "Add Condition", c = "McdAddGeoVisCondition()")
    cmds.setParent("..")
    
    cmds.setParent("..")
    
    
    #------------------------------- Cricial Help --------------------------------#
    child1 = cmds.columnLayout(adj = True)
    cmds.text(l = "* List all the terrains in scene", fn = "smallBoldLabelFont", align = "left")
    cmds.text(l = "      Terrain: Meshes connect to McdTerrain node", align = "left")
    cmds.text(l = '      Attribute: restitution, dynamic and static friction' , align = "left")
    
    cmds.separator(h = 10)
    cmds.button(l = "Check detailed help")
    
    cmds.setParent( '..' )
    
    
    cmds.tabLayout( tabs, edit=True,tabLabel=((child0, "Geo Vis Info in scene"),(child1, "Quick Tips")))
    cmds.showWindow(winName)



def McdRefreshGeoVisManager():
    McdGeoVisManagerGUI()

def McdExitGeoVisManager():
    try:
        cmds.deleteUI("McdGeoVisManager")
    except:
        pass


